 # THE BASICS
 # Integers
 ---
 _**Integers**_ are whole numbers with no fractional component, such as 42 and -23. _**Integers**_ are either signed (positive, zero, or negative) or unsigned (positive or zero).

 Swift provides _**signed and unsigned integers**_ in 8, 16, 32, and 64 bit forms. These _**integers**_ follow a naming convention similar to C, in that an _**8-bit unsigned integer**_ is of type `UInt8`, and a _**32-bit signed integer**_ is of type `Int32`. Like all types in Swift, these _**integer types**_ have capitalized names.
