 ### 07 - Section 2
 # CLOSURES
 # Trailing Closures
 ---
 If you need to pass a _closure expression_ to a function as the function’s final argument and the _closure expression_ is long, it can be useful to write it as a _**trailing closure**_ instead. You write a _**trailing closure**_ after the function call’s parentheses, even though the trailing closure is still an argument to the function. When you use the _**trailing closure**_ syntax, you don’t write the argument label for the first closure as part of the function call. A function call can include multiple _**trailing closures**_; however, the first few examples below use a _**single trailing**_ closure.
