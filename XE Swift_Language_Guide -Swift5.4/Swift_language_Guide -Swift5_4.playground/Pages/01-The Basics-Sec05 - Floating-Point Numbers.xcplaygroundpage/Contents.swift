 # THE BASICS
 # Floating-Point Numbers
 ---
 _**Floating-point numbers**_ are numbers with a fractional component, such as 3.14159, 0.1, and -273.15.

 _**Floating-point types**_ can represent a much wider range of values than _integer types_, and can store numbers that are much larger or smaller than can be stored in an `Int`. Swift provides two _**signed floating-point number types**_:

 + `Double` represents a 64-bit floating-point number.
 + `Float` represents a 32-bit floating-point number.
