 ### 01 - Section 6
 # THE BASICS
 # Type Safety and Type Inference
 ---
 Swift is a _**type-safe**_ language. A _**type safe**_ language encourages you to be clear about the types of values your code can work with. If part of your code requires a `String`, you can’t pass it an `Int` by mistake.

 Because Swift is _**type safe**_, it performs type checks when compiling your code and flags any mismatched types as errors. This enables you to catch and fix errors as early as possible in the development process.

 Type-checking helps you avoid errors when you’re working with different types of values. However, this doesn’t mean that you have to specify the type of every constant and variable that you declare. If you don’t specify the type of value you need, Swift uses _**type inference**_ to work out the appropriate type. _**Type inference**_ enables a compiler to deduce the type of a particular expression automatically when it compiles your code, simply by examining the values you provide.
