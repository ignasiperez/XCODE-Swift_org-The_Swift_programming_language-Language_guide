 # ENUMERATIONS
 # Recursive Enumerations
 ---
 A _**recursive enumeration**_ is an _enumeration_ that has another instance of the enumeration as the _associated value_ for one or more of the enumeration cases. You indicate that an enumeration case is recursive by writing `indirect before it, which tells the compiler to insert the necessary layer of indirection.
