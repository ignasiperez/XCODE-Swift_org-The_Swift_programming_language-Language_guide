import Foundation


// ******************** 03-Sec10-Ex01c ********************

let greeting = "Guten Tag!"

print("\n--- 03-Sec10-Ex01c ---")

for index in greeting.indices {
  print("\(greeting[index]) ", terminator: "")
}

