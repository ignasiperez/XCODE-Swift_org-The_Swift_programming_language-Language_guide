import Foundation


// ******************** 05-Sec01-Ex01a ********************

print("\n--- 05-Sec01-Ex01a ---")

let names = ["Anna", "Alex", "Brian", "Jack"]

for name in names {
  print("Hello, \(name)!")
}
// Hello, Anna!
// Hello, Alex!
// Hello, Brian!
// Hello, Jack!

