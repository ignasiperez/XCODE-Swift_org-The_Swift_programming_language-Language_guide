import Foundation


// ******************** 02-Sec07-Ex02 ********************

print("\n--- 02-Sec07-Ex02 ---")

let names = ["Anna", "Alex", "Brian", "Jack"]
let count = names.count

for i in 0..<count {
    print("Person \(i + 1) is called \(names[i])")
}
// Person 1 is called Anna
// Person 2 is called Alex
// Person 3 is called Brian
// Person 4 is called Jack

