 # Ternary Conditional Operator
 ---
 The _**ternary conditional operator**_ is a special operator with three parts, which takes the form `question ? answer1 : answer2`. It’s a shortcut for evaluating one of two expressions based on whether question is true or false. If question is true, it evaluates `answer1` and returns its value; otherwise, it evaluates `answer2` and returns its value.
