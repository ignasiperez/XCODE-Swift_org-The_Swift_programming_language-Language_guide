 You use _**error handling**_ to respond to error conditions your program may encounter during execution.

 In contrast to _optionals_, which can use the presence or absence of a value to communicate success or failure of a function, _**error handling**_ allows you to determine the underlying cause of failure, and, if necessary, propagate the error to another part of your program.
