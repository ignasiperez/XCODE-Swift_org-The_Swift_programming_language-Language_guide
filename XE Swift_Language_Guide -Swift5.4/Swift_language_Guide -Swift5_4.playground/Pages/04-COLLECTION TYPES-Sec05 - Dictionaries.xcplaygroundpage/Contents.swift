 ### 04 - Section 5
 # COLLECTION TYPES
 # Dictionaries
 ---
 A _**dictionary**_ stores associations between keys of the same type and values of the same type in a collection with no defined ordering. Each value is associated with a unique key, which acts as an identifier for that value within the _dictionary_. Unlike items in an array, items in a _dictionary_ don’t have a specified order. You use a _dictionary_ when you need to look up values based on their identifier, in much the same way that a real-world dictionary is used to look up the definition for a particular word.
