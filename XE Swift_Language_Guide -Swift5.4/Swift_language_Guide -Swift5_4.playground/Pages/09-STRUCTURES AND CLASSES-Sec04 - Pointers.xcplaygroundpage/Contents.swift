 If you have experience with C, C++, or Objective-C, you may know that these languages use _**pointers**_ to refer to addresses in memory. A Swift constant or variable that refers to an instance of some reference type is similar to a _pointer_ in C, but isn’t a direct pointer to an address in memory, and doesn’t require you to write an asterisk (*) to indicate that you are creating a reference. Instead, these references are defined like any other constant or variable in Swift.
