 ### 02 - Section 6
 # BASIC OPERATORS
 # Nil-Coalescing Operator
 ---
 The _**nil-coalescing operator**_ (`a ?? b`) unwraps an `optional a` if it contains a value, or returns a default value `b` if a is `nil`. The expression `a` is always of an _optional type_. The expression `b` must match the type that’s stored inside `a`.
